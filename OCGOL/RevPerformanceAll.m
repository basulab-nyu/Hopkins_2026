data = [control(:); treatment(:)];

% Group factor: 1 = control, 2 = treatment
group = [repmat(1, nControl * nTimePoints, 1); ...
         repmat(2, nTreatment * nTimePoints, 1)];

% Time factor: 1 to nTimePoints repeated across subjects
timeControl = repmat((1:nTimePoints), nControl, 1);
timeTreatment = repmat((1:nTimePoints), nTreatment, 1);
time = [timeControl(:); timeTreatment(:)];

% Remove entries with missing data (NaNs)
validIdx = ~isnan(data);
data_clean = data(validIdx);
group_clean = group(validIdx);
time_clean = time(validIdx);

% Run two-way ANOVA with interaction
[p, tbl, stats] = anovan(data_clean, {group_clean, time_clean}, ...
                         'model', 'interaction', ...
                         'varnames', {'Group', 'Time'});
                     
% Run post-hoc tests on Group × Time interaction
comparisonResults = multcompare(stats, 'Dimension', [1 2]);

nTimePoints = 5;  % Adjust as needed

% comparisonResults(i, :) = [idx1, idx2, CI_low, meanDiff, CI_high, pval]
for t = 1:nTimePoints
    idxControl = t;
    idxTreatment = t + nTimePoints;
    
    % Find the corresponding comparison
    row = find((comparisonResults(:,1) == idxControl & comparisonResults(:,2) == idxTreatment) | ...
               (comparisonResults(:,1) == idxTreatment & comparisonResults(:,2) == idxControl));
    
    if ~isempty(row)
        diff = comparisonResults(row, 4);
        pval = comparisonResults(row, 6);
        fprintf('Time %d: mean difference = %.3f, p = %.4f\n', t, diff, pval);
    else
        fprintf('Time %d: comparison not found.\n', t);
    end
end
