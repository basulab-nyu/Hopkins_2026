for i_mouse = 1:length(control_mice)
    for i_session=1:numSessions;
        try
        load(control_mice{i_mouse}{i_session});

        performance.control.fracCorrAr(i_mouse,i_session) = sum(Behavior.performance.trialOrder==2)/sum(Behavior.performance.trialOrder==2|Behavior.performance.trialOrder==20);
        performance.control.fracCorrBr(i_mouse,i_session) =  sum(Behavior.performance.trialOrder==3)/sum(Behavior.performance.trialOrder==3|Behavior.performance.trialOrder==30);
        performance.control.fracCorrAll(i_mouse,i_session) =  sum(Behavior.performance.trialOrder==2|Behavior.performance.trialOrder==3)/length(Behavior.performance.trialOrder);

        %% Lick Histograms for all laps
        %trials
        %segment the lick counts into laps and trials
        %count the number of licks wihin 10cm of the reward zone and divide by the
        %total licks for the entire lap
        %take average of fractions across laps
        start_lap_idx = find(Behavior.time == Behavior.lap{1}(1));
        end_lap_idx = find(Behavior.time == Behavior.lap{end}(2));
        rew_size = 10;

        for ll = 1:size(Behavior.lap,2) %outputs cell array with each cell being a lap and within each cell the time and position of licks are recorded
           lick_idx_temp = find(Behavior.lick.time >= Behavior.lap{ll}(1) &   Behavior.lick.time <= Behavior.lap{ll}(2));
           lick_lap{ll}.time = Behavior.lick.time(lick_idx_temp);
           lick_lap{ll}.position = Behavior.lick.position(lick_idx_temp);
           %set temp variable to empty
           lick_idx_temp = [];
        end


        %calulate mean and median of A and B trial reward zones onsets
        %median
        med_A_reward = median(Behavior.rewards{1}.position);
        med_B_reward = median(Behavior.rewards{2}.position);

        %mean
        mean_A_reward = mean(Behavior.rewards{1}.position);
        mean_B_reward = mean(Behavior.rewards{2}.position);

        %bin edges for raster
        edges = (0:2:200);

        %bin licks on each lap
        for ll = 1:size(Behavior.lap,2)
            [N_licks(ll,:),~,~] = histcounts(lick_lap{1,ll}.position,edges,'Normalization','count');
        end

        %% Fraction Licks in Reward Zone and Anticipatory Zone
        %A trials
        A_index = find(Behavior.lap_id.trial_based==2);
        B_index = find(Behavior.lap_id.trial_based==3);
        A_total_licks = 0;
        A_reward_licks = 0;
        B_total_licks = 0;
        B_reward_licks = 0;
        A_ant_licks = 0;
        B_ant_licks = 0;

%Code in case performance is to be calculated as an average of score on each lap     
%         test=NaN([length(A_index),1]);
%         
%         for lapNum = 1:length(A_index)
%             test_lap_licks = length(lick_lap{1,A_index(lapNum)}.position);
%             test_reward_licks = length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward & lick_lap{1,A_index(lapNum)}.position <= med_A_reward+rew_size));
%             test_frac_lick_lap = test_reward_licks/test_lap_licks;
%             test(lapNum)=test_frac_lick_lap;
%         end 
        
        for lapNum = 1:length(A_index)
            A_total_licks = A_total_licks + length(lick_lap{1,A_index(lapNum)}.position);
            A_reward_licks = A_reward_licks + length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward & lick_lap{1,A_index(lapNum)}.position <= med_A_reward+rew_size));
        end 

        for lapNum = 1:length(B_index)
            B_total_licks = B_total_licks + length(lick_lap{1,B_index(lapNum)}.position);
            B_reward_licks = B_reward_licks + length(find(lick_lap{1,B_index(lapNum)}.position >= med_B_reward & lick_lap{1,B_index(lapNum)}.position <= med_B_reward+rew_size));
        end
        
        for lapNum = 1:length(A_index)
            A_ant_licks = A_ant_licks + length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward-rew_size & lick_lap{1,A_index(lapNum)}.position <= med_A_reward));
        end
        
        for lapNum = 1:length(B_index)
            B_ant_licks = B_ant_licks + length(find(lick_lap{1,B_index(lapNum)}.position >= med_B_reward-rew_size & lick_lap{1,B_index(lapNum)}.position <= med_B_reward));
        end   
        performance.control.fracLickAr(i_mouse,i_session) = A_reward_licks/A_total_licks;
        performance.control.fracLickBr(i_mouse,i_session) = B_reward_licks/B_total_licks;
        performance.control.fracAntLickAr(i_mouse,i_session) = A_ant_licks/A_total_licks;
        performance.control.fracAntLickBr(i_mouse,i_session) = B_ant_licks/B_total_licks;
        end
    end
