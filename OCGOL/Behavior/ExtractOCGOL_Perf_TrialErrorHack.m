function ExtractOCGOL_Perf_TrialErrorHack(sessions)

    %% Computations
    Performance.PropCorrect.A= NaN(1,length(sessions));
    Performance.PropCorrect.B= NaN(1,length(sessions)); 
    Performance.PropCorrect.All= NaN(1,length(sessions));
    Performance.NumLap= NaN(1,length(sessions));
    
    for i_session=1:length(sessions);
        disp(sessions(i_session))
        load(sessions{i_session});
        try 
            %%
            %load in behavior file from manual list of sessions (looping through
            %folders aka sessions)
            %% Trial Performance
            Performance.PropCorrect.A(i_session)= sum(Behavior.performance.trialOrder==2)/sum(Behavior.lap_id.trial_based==2);
            Performance.PropCorrect.B(i_session)= sum(Behavior.performance.trialOrder==3)/sum(Behavior.lap_id.trial_based==3);
            Performance.PropCorrect.All(i_session)=sum(Behavior.performance.trialCorrect==1)/size(Behavior.performance.trialCorrect,1);
    
            Performance.NumLap(i_session)=size(Behavior.lap,2);
    
            %% Lick Histograms for all laps
            %trials
            %segment the lick counts into laps and trials
            %count the number of licks wihin 10cm of the reward zone and divide by the
            %total licks for the entire lap
            %take average of fractions across laps
            start_lap_idx = find(Behavior.time == Behavior.lap{1}(1));
            end_lap_idx = find(Behavior.time == Behavior.lap{end}(2));
            rew_size = 10;
    
            for ll = 1:size(Behavior.lap,2) %outputs cell array with each cell being a lap and within each cell the time and position of licks are recorded
               lick_idx_temp = find(Behavior.lick.time >= Behavior.lap{ll}(1) &   Behavior.lick.time <= Behavior.lap{ll}(2));
               lick_lap{ll}.time = Behavior.lick.time(lick_idx_temp);
               lick_lap{ll}.position = Behavior.lick.position(lick_idx_temp);
               %set temp variable to empty
               lick_idx_temp = [];
            end
    
    %         %get reward position on each lap
    %         %reward postion
    %         reward_position = zeros(size(Behavior.lap,2),1);
    %         reward_pos_idx = zeros(size(Behavior.lap,2),1);
    % 
    %         %early rewards - B trials 
    %         reward_position(find(Behavior.lap_id.trial_based==3))= Behavior.rewards{1,1}.full_laps.position;
    %         %idxs of associated reward positions
    %         reward_pos_idx(find(Behavior.lap_id.trial_based==3))= Behavior.rewards{1,1}.full_laps.idx;
    %         %late rewards - A trials
    %         reward_position(find(Behavior.lap_id.trial_based==2))= Behavior.rewards{1,2}.full_laps.position;
    %         %idxs of associated reward positions
    %         reward_pos_idx(find(Behavior.lap_id.trial_based==2))= Behavior.rewards{1,2}.full_laps.idx;
    
    
            %calulate mean and median of A and B trial reward zones onsets
            %median
            med_A_reward = median(Behavior.rewards{2}.position);
            med_B_reward = median(Behavior.rewards{1}.position);
    
            %mean
            mean_A_reward = mean(Behavior.rewards{2}.position);
            mean_B_reward = mean(Behavior.rewards{1}.position);
    
            %bin edges for raster
            edges = (0:2:200);
    
            %bin licks on each lap
            for ll = 1:size(Behavior.lap,2)
                [N_licks(ll,:),~,~] = histcounts(lick_lap{1,ll}.position,edges,'Normalization','count');
            end
    
            Performance.Lick.LickRaster.A{1,i_session}= N_licks(find(Behavior.lap_id.trial_based==2),:);
            Performance.Lick.LickRaster.B{1,i_session}= N_licks(find(Behavior.lap_id.trial_based==3),:);
            %% Fraction Licks in Reward Zone and Anticipatory Zone
            %A trials
            A_index = find(Behavior.lap_id.trial_based==2);
            B_index = find(Behavior.lap_id.trial_based==3);
            A_total_licks = 0;
            A_reward_licks = 0;
            B_total_licks = 0;
            B_reward_licks = 0;
            A_ant_licks = 0;
            B_ant_licks = 0;
    
    %Code in case performance is to be calculated as an average of score on each lap     
    %         test=NaN([length(A_index),1]);
    %         
    %         for lapNum = 1:length(A_index)
    %             test_lap_licks = length(lick_lap{1,A_index(lapNum)}.position);
    %             test_reward_licks = length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward & lick_lap{1,A_index(lapNum)}.position <= med_A_reward+rew_size));
    %             test_frac_lick_lap = test_reward_licks/test_lap_licks;
    %             test(lapNum)=test_frac_lick_lap;
    %         end 
            
            for lapNum = 1:length(A_index)
                A_total_licks = A_total_licks + length(lick_lap{1,A_index(lapNum)}.position);
                A_reward_licks = A_reward_licks + length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward & lick_lap{1,A_index(lapNum)}.position <= med_A_reward+rew_size));
            end 
    
            for lapNum = 1:length(B_index)
                B_total_licks = B_total_licks + length(lick_lap{1,B_index(lapNum)}.position);
                B_reward_licks = B_reward_licks + length(find(lick_lap{1,B_index(lapNum)}.position >= med_B_reward & lick_lap{1,B_index(lapNum)}.position <= med_B_reward+rew_size));
            end
            
            for lapNum = 1:length(A_index)
                A_ant_licks = A_ant_licks + length(find(lick_lap{1,A_index(lapNum)}.position >= med_A_reward-rew_size & lick_lap{1,A_index(lapNum)}.position <= med_A_reward));
            end
            
            for lapNum = 1:length(B_index)
                B_ant_licks = B_ant_licks + length(find(lick_lap{1,B_index(lapNum)}.position >= med_B_reward-rew_size & lick_lap{1,B_index(lapNum)}.position <= med_B_reward));
            end   
            Performance.Lick.FracLicksReward.A(i_session) = A_reward_licks/A_total_licks;
            Performance.Lick.FracLicksReward.B(i_session) = B_reward_licks/B_total_licks;
            Performance.Lick.FracLicksAnt.A(i_session) = A_ant_licks/A_total_licks;
            Performance.Lick.FracLicksAnt.B(i_session) = B_ant_licks/B_total_licks;
    
    %         speed of mouse +/- 2 seconds from reward zone
    %         get 1st index of position of interest
    %         sample rate is 0.0001 seconds
    %         2 seconds = 20000 data points
    %         take time and position starting at time(indexOfInterest-20000) and position(indexOfInterest-20000) 
    %         calculate speed(currIndex) = (position(currIndex) - position(currIndex+1))/(time(currIndex) - time(currIndex+1))
    %         go to next position of interest and repeat
    %         after calculating speeds for all positions, take average of each speed index
    %% Number of erroneous licks + number rewards collected
            A_error_licks=0;
            B_error_licks=0;
    
            for lapNum = 1:length(A_index)
                A_error_licks = A_error_licks + length(find(lick_lap{1,A_index(lapNum)}.position >= med_B_reward & lick_lap{1,A_index(lapNum)}.position <= med_B_reward+rew_size));
            end
            
            for lapNum = 1:length(B_index)
                B_error_licks = B_error_licks + length(find(lick_lap{1,B_index(lapNum)}.position >= med_A_reward & lick_lap{1,B_index(lapNum)}.position <= med_A_reward+rew_size));
            end        
            
            Performance.Lick.NumErrors.A(i_session) = A_error_licks;
            Performance.Lick.NumErrors.B(i_session) = B_error_licks;
     %% Rewards collected       
            rewardsCollLap_ID= NaN(length(Behavior.reward_coll.idx),1);
            rewCollPerLap = NaN(length(Behavior.lap_id.trial_based),1);
    
            for i_reward=1:length(Behavior.reward_coll.idx)
                rewardsCollLap_ID(i_reward)=Behavior.lapNb(Behavior.reward_coll.idx(i_reward));
            end
            
            for i_lap=1:length(Behavior.lap_id.trial_based)
                rewCollPerLap(i_lap)=sum(rewardsCollLap_ID==i_lap);
            end
    
            rewCollPerLap_A = rewCollPerLap(Behavior.lap_id.trial_based==2);
            rewCollPerLap_B = rewCollPerLap(Behavior.lap_id.trial_based==3);
              
            Performance.Lick.RewardsColl.A{i_session} = rewCollPerLap_A;
            Performance.Lick.RewardsColl.B{i_session} = rewCollPerLap_B;
    
            %% Average Speed +/- 2 seconds center on reward zone entrance (for incorrect reward zone use the mean from the session)
            %This section creates a downsampled (by 1000) vector for position, time,
            %and speed for each lap
            lap_index = find(Behavior.lap_binary==1); %indices of the lap start (this may include an extra lap that is not is a full lap)
    
            for ll = 1:length(lap_index) %outputs cell array with each cell being a lap and within each cell the position of mouse is recorded
               if ll==length(lap_index)
                   pos_lap{ll}.time = downsample(Behavior.time(lap_index(ll):end),1000);
                   pos_lap{ll}.position = downsample(Behavior.position(lap_index(ll):end),1000);
               else
                   pos_lap{ll}.time = downsample(Behavior.time(lap_index(ll):lap_index(ll+1)-1),1000);
                   pos_lap{ll}.position = downsample(Behavior.position(lap_index(ll):lap_index(ll+1)-1),1000);
               end
            end
    
            for ll=1:length(lap_index)%creates the downsampled speed vector
                v=zeros(length(pos_lap{ll}.time)-1,1);
                for ii=1:length(pos_lap{ll}.time)-1
                    v(ii) = (pos_lap{ll}.position(ii+1)-pos_lap{ll}.position(ii))/(pos_lap{ll}.time(ii+1)-pos_lap{ll}.time(ii));
                end
                pos_lap{ll}.speed= v;
            end
            %% Creating matrices with the speed around each reward zone for each lap
            timeBefore = 20; %20
            timeAfter = 20; %20
            
            %includes running speed for correct reward zone and incorrect reward zone
            A_ARewZone = NaN(length(A_index),(1+timeBefore+timeAfter));
            A_BRewZone = NaN(length(A_index),(1+timeBefore+timeAfter));
    
            for tt=1:length(A_index)
                rz = find(pos_lap{1,A_index(tt)}.position >= med_A_reward,1);
                if rz+timeAfter> length(pos_lap{1,A_index(tt)}.speed) %Handles exceptions where mouse runs too fast and there is not 2 seconds after reward zone a
                    timeAfter_mod= length(pos_lap{1,A_index(tt)}.speed)-rz;
                    speed_trace = pos_lap{1,A_index(tt)}.speed(rz-timeBefore:rz+timeAfter_mod);
                    A_ARewZone(tt,1:length(speed_trace))=speed_trace;
                else
                    speed_trace = pos_lap{1,A_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
                    A_ARewZone(tt,:)=speed_trace;
                end
            end
    
            for tt=1:length(A_index)
                rz= find(pos_lap{1,A_index(tt)}.position >= med_B_reward,1);
                speed_trace = pos_lap{1,A_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
                A_BRewZone(tt,:)=speed_trace;
            end
    
            B_BRewZone = NaN(length(B_index),(1+timeBefore+timeAfter));
            B_ARewZone = NaN(length(B_index),(1+timeBefore+timeAfter));
    
            for tt=1:length(B_index)
                rz = find(pos_lap{1,B_index(tt)}.position >= med_B_reward,1);
                speed_trace = pos_lap{1,B_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
                B_BRewZone(tt,:) = speed_trace;
            end
            for tt=1:length(B_index)
                rz = find(pos_lap{1,B_index(tt)}.position >= med_A_reward,1);
                if rz+timeAfter> length(pos_lap{1,B_index(tt)}.speed) %Handles exceptions where mouse runs too fast and there is not 2 seconds after reward zone a
                    timeAfter_mod= length(pos_lap{1,B_index(tt)}.speed)-rz;
                    speed_trace = pos_lap{1,B_index(tt)}.speed(rz-timeBefore:rz+timeAfter_mod);
                    B_ARewZone(tt,1:length(speed_trace))=speed_trace;
                else
                    speed_trace= pos_lap{1,B_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
                    B_ARewZone(tt,:)=speed_trace;
                end
            end
    
            Performance.Speed.A.AReward{1,i_session} = A_ARewZone;
            Performance.Speed.A.BReward{1,i_session} = A_BRewZone;
            Performance.Speed.B.BReward{1,i_session} = B_BRewZone;
            Performance.Speed.B.AReward{1,i_session} = B_ARewZone;
            disp('Done processing!')
    
        catch e
            disp(e);
        end
    end
    
    mouse_path = sessions{1};
    tokens = regexp(mouse_path, '\\(MH\d+)', 'tokens', 'once');
    mouseID = tokens{1};
    saveDir = fullfile('R:\MH\LEC_Bilateral_Silencing\Stacks', mouseID);
    if ~exist(saveDir, 'dir')
        mkdir(saveDir);
    end
    fileName = sprintf('%s_Perf_Silence_V3.mat', mouseID);
    savePath = fullfile(saveDir, fileName);
    save(savePath, 'Performance');
    fprintf('Saved Performance to %s\n', savePath);
end

