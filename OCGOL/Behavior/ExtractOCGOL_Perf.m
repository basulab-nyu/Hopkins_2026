        % take time and position starting at time(indexOfInterest-20000) and position(indexOfInterest-20000) 
        % calculate speed(currIndex) = (position(currIndex) - position(currIndex+1))/(time(currIndex) - time(currIndex+1))
        % go to next position of interest and repeat
        % after calculating speeds for all positions, take average of each speed index
        %% Average Speed +/- 2 seconds center on reward zone entrance (for incorrect reward zone use the mean from the session)
        %This section creates a downsampled (by 1000) vector for position, time,
        %and speed for each lap
        lap_index = find(Behavior.lap_binary==1); %indices of the lap start (this may include an extra lap that is not is a full lap)

        for ll = 1:length(lap_index) %outputs cell array with each cell being a lap and within each cell the position of mouse is recorded
           if ll==length(lap_index)
               pos_lap{ll}.time = downsample(Behavior.time(lap_index(ll):end),1000);
               pos_lap{ll}.position = downsample(Behavior.position(lap_index(ll):end),1000);
           else
               pos_lap{ll}.time = downsample(Behavior.time(lap_index(ll):lap_index(ll+1)-1),1000);
               pos_lap{ll}.position = downsample(Behavior.position(lap_index(ll):lap_index(ll+1)-1),1000);
           end
        end

        for ll=1:length(lap_index)%creates the downsampled speed vector
            v=zeros(length(pos_lap{ll}.time)-1,1);
            for ii=1:length(pos_lap{ll}.time)-1
                v(ii) = (pos_lap{ll}.position(ii+1)-pos_lap{ll}.position(ii))/(pos_lap{ll}.time(ii+1)-pos_lap{ll}.time(ii));
            end
            pos_lap{ll}.speed= v;
        end
        %% Creating matrices with the speed around each reward zone for each lap
        timeBefore = 20; %20
        timeAfter = 20; %20
        
        %includes running speed for correct reward zone and incorrect reward zone
        A_ARewZone = NaN(length(A_index),(1+timeBefore+timeAfter));
        A_BRewZone = NaN(length(A_index),(1+timeBefore+timeAfter));

        for tt=1:length(A_index)
            rz = find(pos_lap{1,A_index(tt)}.position >= Behavior.rewards{1,2}.full_laps.position(tt),1);
            speed_trace = pos_lap{1,A_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
            A_ARewZone(tt,:)=speed_trace;
        end

        for tt=1:length(A_index)
            rz= find(pos_lap{1,A_index(tt)}.position >= mean_B_reward);
            speed_trace = pos_lap{1,A_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
            A_BRewZone(tt,:)=speed_trace;
        end

        B_BRewZone = NaN(length(B_index),(1+timeBefore+timeAfter));
        B_ARewZone = NaN(length(B_index),(1+timeBefore+timeAfter));

        for tt=1:length(B_index)
            rz = find(pos_lap{1,B_index(tt)}.position >= Behavior.rewards{1,1}.full_laps.position(tt),1);
            speed_trace = pos_lap{1,B_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
            B_BRewZone(tt,:) = speed_trace;
        end
        for tt=1:length(B_index)
            rz = find(pos_lap{1,B_index(tt)}.position >= mean_A_reward,1);
            speed_trace= pos_lap{1,B_index(tt)}.speed(rz-timeBefore:rz+timeAfter);
            B_ARewZone(tt,:)=speed_trace;
        end

        Performance.Speed.A.AReward{1,i_session} = A_ARewZone;
        Performance.Speed.A.BReward{1,i_session} = A_BRewZone;
        Performance.Speed.B.BReward{1,i_session} = B_BRewZone;
        Performance.Speed.B.AReward{1,i_session} = B_ARewZone;
        disp('Done processing!')
    catch e
        disp(e);
    end
end

